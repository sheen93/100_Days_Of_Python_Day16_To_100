piano_keys = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]

piano_tuples = ("do", "re", "mi", "fa", "so")

print(piano_keys[2:5]) #sliced position 2 to 4

print(piano_keys[3:]) #sliced position 3 to end

print(piano_keys[:6]) # sliced position start to 5

print(piano_keys[2::2]) #sliced position 2 to end skipping 1 step

print(piano_keys[::2]) #sliced position beginning to end skipping 1 step

print(piano_tuples[2::2]) #slicing works the same with tuples as lists